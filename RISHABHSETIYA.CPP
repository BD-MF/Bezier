void bezier (int x[],int y[])
{
int i;
double t,xt,yt;
for (t = 0.0; t < 1.0; t += 0.005)
{
xt = (1-t)*(1-t)*(1-t)*x[0]+3*t*(1-t)*(1-t)*x[1]+3*t*t*(1-t)*x[2]+t*t*t*x[3];
yt = (1-t)*(1-t)*(1-t)*y[0]+3*t*(1-t)*(1-t)*y[1]+3*t*t*(1-t)*y[2]+t*t*t*y[3];
putpixel (xt, yt,WHITE);
}

}

